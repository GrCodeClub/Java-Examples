public class Main {
    public static void main(String[] args) {
        int i = 1; // Αρχική τιμή

        // Επανάληψη όσο η τιμή του i είναι μικρότερη ή ίση με 5
        while (i <= 5) {
            System.out.println("Αριθμός: " + i);
            i++; // Αύξηση της τιμής του i κατά 1
        }
    }
}
